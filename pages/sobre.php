<h1>Sobre Nos:</h1>
<p>Nossa Locadora surge através de uma iniciativa escolar feita pelo nosso digníssimo professor Samuel.
Mais do que um simples projeto, ela representa o poder transformador da educação quando aliada à criatividade e ao espírito empreendedor. Cada filme, cada título em nossa prateleira carrega um propósito maior: mostrar que grandes ideias podem nascer dentro da sala de aula e ganhar vida com empenho, colaboração e visão de futuro.

Este é um espaço criado por jovens sonhadores, guiados por um educador que acreditou no potencial de seus alunos. É prova viva de que quando há incentivo, os talentos florescem. Aqui, aprendemos não só sobre filmes, mas sobre responsabilidade, trabalho em equipe, gestão e, acima de tudo, sobre acreditar em nós mesmos.

Nossa Locadora não é apenas um negócio escolar — é um símbolo de que quando se planta educação, colhe-se transformação. Que ela inspire outros alunos, professores e comunidades a enxergarem a escola como o ponto de partida para grandes realizações.
</p>

